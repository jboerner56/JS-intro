// function that when given a start and end number, prints each number between. each on a new line
// for loop
function printNumbers(start, end){
    for(let count = start; count <= end; count++){
        console.log(count)
    }
}
printNumbers(1, 10)
// while loop
function printNumbers(start, end){
    let count = start;
    while(count <= end){
        console.log(count)
    }
}
printNumbers(1, 10)

